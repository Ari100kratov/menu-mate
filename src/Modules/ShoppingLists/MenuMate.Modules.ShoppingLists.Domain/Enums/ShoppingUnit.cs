namespace MenuMate.Modules.ShoppingLists.Domain.Enums;

/// <summary>
/// Единица измерения в списке покупок.
/// </summary>
public enum ShoppingUnit
{
    /// <summary>
    /// Граммы.
    /// </summary>
    Gram = 0,

    /// <summary>
    /// Килограммы.
    /// </summary>
    Kilogram = 1,

    /// <summary>
    /// Миллилитры.
    /// </summary>
    Milliliter = 2,

    /// <summary>
    /// Литры.
    /// </summary>
    Liter = 3,

    /// <summary>
    /// Штуки.
    /// </summary>
    Piece = 4,

    /// <summary>
    /// Чайная ложка.
    /// </summary>
    Teaspoon = 5,

    /// <summary>
    /// Столовая ложка.
    /// </summary>
    Tablespoon = 6,

    /// <summary>
    /// Щепотка.
    /// </summary>
    Pinch = 7,

    /// <summary>
    /// Упаковка.
    /// </summary>
    Pack = 8,

    /// <summary>
    /// Количество по вкусу.
    /// </summary>
    ToTaste = 9,

    /// <summary>
    /// Единица не определена.
    /// </summary>
    Unknown = 10
}

